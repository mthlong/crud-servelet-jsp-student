package entity;

import java.util.Date;

/**
 *
 * @author THANHDAT-PC
 */
public class Student {

    private int id;
    private String fullName;
    private String gender;
    private Date DOB;

    public Student() {
    }

    public Student(int id, String fullName, String gender, Date DOB) {
        this.id = id;
        this.fullName = fullName;
        this.gender = gender;
        this.DOB = DOB;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getFullName() {
        return fullName;
    }

    public void setFullName(String fullName) {
        this.fullName = fullName;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public Date getDOB() {
        return DOB;
    }

    public void setDOB(Date DOB) {
        this.DOB = DOB;
    }
    
    
}
